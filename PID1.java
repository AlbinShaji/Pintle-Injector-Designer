    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {                                      
        // TODO add your handling code here:        
        
        
        // Collecting the inputs
        double k = Double.parseDouble(K.getText());
        double pe = Double.parseDouble(Pe.getText());
        double pi = Double.parseDouble(Pi.getText());
        double f = Double.parseDouble(F.getText());
        double ti = Double.parseDouble(Ti.getText());
        double mr = Double.parseDouble(Mr.getText());
        double r = Double.parseDouble(R.getText());
        
        //Calculating the CF        
        double a,b,c,d;
        a = 2*pow(k,2)/(k-1);//System.out.println("a="+a);
        b = pow((2/(k+1)),((k+1)/(k-1)));//System.out.println("b="+b);
        c = 1-pow((pe/pi),((k-1)/k));//System.out.println("c="+c);
        cf = Math.sqrt(a*b*c);
        CF.setText(""+cf);
        
        //Calculating At
        at = f/(pi*cf);
        At.setText(""+at+" sq.meter");
        
        //Calculating CV(C*)
        d= pow((2/(k+1)),((k+1)*(k-1)));
        cv = Math.sqrt(k*r*ti)/(k*Math.sqrt(b));
        CV.setText(""+cv);
        
        //Calculating MFR
        mfr = pi*at/cv;
        mfrf = mfr/(mr+1);
        mfro = mfr-mfrf;
        MFR.setText(""+mfr+" Kg/sec");
        MFRf.setText(""+mfrf+" Kg/sec");
        MFRo.setText(""+mfro+" Kg/sec");
        next.setEnabled(true);
        jButton1.setEnabled(false);
    }                                     

